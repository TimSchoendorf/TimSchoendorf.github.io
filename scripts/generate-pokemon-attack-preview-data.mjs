function familyFor(move) {
  if (EXACT.has(move.id)) return EXACT.get(move.id);
  if (move.type === 'Fire') return FAMILY.FIRE;
  if (move.type === 'Water') return FAMILY.WATER;
  if (move.type === 'Electric') return FAMILY.ELECTRIC;
  if (move.type === 'Ice') return FAMILY.ICE;
  if (move.type === 'Rock' || move.type === 'Ground') return FAMILY.ROCK;
  if (move.type === 'Grass') return move.category === 'Status' ? FAMILY.STATUS_SELF : FAMILY.VINE;
  if (move.type === 'Psychic') return FAMILY.CONFUSION;
  if (move.category === 'Status') return FAMILY.STATUS_SELF;
  if (move.category === 'Special') return FAMILY.BEAM;
  return FAMILY.IMPACT;
}

async function main() {
  const moves = dex.moves.all()
    .filter((move) => move.exists && move.gen === 1 && !move.isNonstandard)
    .sort((a, b) => a.num - b.num)
    .map((move) => ({
      id: move.id,
      num: move.num,
      name: move.name,
      type: move.type,
      category: move.category,
      pp: move.pp,
      power: move.basePower,
      accuracy: move.accuracy === true ? 100 : move.accuracy,
      family: familyFor(move),
    }));

  await fs.writeFile(OUTPUT, JSON.stringify(moves, null, 2), 'utf8');
  console.log(`Wrote ${moves.length} Gen 1 moves to ${OUTPUT}`);
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
